SELECT COUNT (DISTINCT p.[Код послуги]) as [Кількість послуг], s.[Назва санаторію] as Санаторіій
FROM Послуги p INNER JOIN [Запис санаторію] ON p.[Код послуги]=[Запис санаторію].[Код послуги]
INNER JOIN Санаторій s ON s.[Код санаторію]=[Запис санаторію].[Код санаторію]
GROUP BY s.[Назва санаторію]